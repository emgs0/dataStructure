/**
 * The {@code Node} class represents a node in a graph.
 * Each node contains a character data.
 */
public class Node {
    /**
     * The character data of the node.
     */
    char data;

    /**
     * Constructs a new {@code Node} instance with the specified character data.
     *
     * @param data The character data of the node.
     */
    Node(char data){
        this.data = data;
    }
}
